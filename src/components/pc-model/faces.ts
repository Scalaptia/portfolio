// PC FACES
// Each face is a 12x12 grid. Characters:
//   . = empty (background)
//   # = main color (foreground)
//   @ = accent color (blush, tongue, etc)
//   O = hollow effect (draws background color inside)
//   Any letter/symbol = drawn as text

